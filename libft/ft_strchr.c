#include "libft.h"

char	*ft_strchr(char const *s, int c)
{
	int	i;

	c = (char)c;
	i = 0;
	while (s[i] != c)
	{
		if (s[i] == '\0')
			return (0);
		i++;
	}
	return ((char *)(s + i));
}
